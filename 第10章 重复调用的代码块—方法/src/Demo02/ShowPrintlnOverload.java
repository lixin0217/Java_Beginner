package Demo02;

public class ShowPrintlnOverload {
    public static void main(String[] args) {
        System.out.println(123); //输出整型int
        System.out.println(12.3); //输出双精度型double
        System.out.println('A');//输出字符型char
        System.out.println(false);//输出布尔型boolean
        System.out.println("Hello Java");//输出字符串类型String
    }
}

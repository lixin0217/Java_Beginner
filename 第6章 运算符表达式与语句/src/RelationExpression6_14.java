/**
 * 【范例6-14】 简单的关系表达式的使用（RelationExpression6 14.java）。
 */
public class RelationExpression6_14 {
    public static void main(String[] args) {
        int a = 5, b = 4;

        boolean t1 = a > b;
        boolean t2 = a == b;
        System.out.println("a > b :" + t1);
        System.out.println("a == b :" + t2);
    }
}

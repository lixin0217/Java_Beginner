/**
 * 【范例6-16】 简单的赋值表达式的使用（AssignExpress6 16.java）。
 */
public class AssignExpression6_16 {
    public static void main(String[] args) {
        int x = 100;
        int y = 20;
        int z;
        z = x * y - 100;
        System.out.println("x = " + x + ",y = " + y);
        System.out.println("x * y - 100 = " + z);
    }
}

/**
 * 【范例6-11】 程序的简洁写法（代码SimpleWriting6 11.java）。
 */
public class SimpleWriting6_11 {
    public static void main(String[] args) {
        int a = 5, b = 8;
        System.out.println("改变之前的数是：a = " + a + ",b = " + b);
        a += b;
        System.out.println("改变之后的数是：a = " + a + ",b = " + b);
    }
}

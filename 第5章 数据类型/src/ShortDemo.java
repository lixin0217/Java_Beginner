/**
 * 【范例5-2】 short类型数据的使用（代码shortdemo.java）。
 */
public class ShortDemo {
    public static void main(String[] args) {
        short short_max = Short.MAX_VALUE;
        short short_min = Short.MIN_VALUE;
        short short_size = Short.SIZE;
        String short_type = String.valueOf(Short.TYPE);
        System.out.println("Short.MAX_VALUE = " + short_max);
        System.out.println("Short.MIN_VALUE = " + short_min);
        System.out.println("Short.SIZE = " + short_size);
        System.out.println("Short.TYPE = " + short_type);
    }
}
